<?php

class Calculation{

    public function add($num1, $num2)
    {
        echo ($num1+$num2);   
    }

    public function minus($num1, $num2)
    {
        echo ($num1-$num2);   
    }

    public function multiply($num1, $num2)
    {
        echo ($num1*$num2);   
    }

    public function divide($num1, $num2)
    {
        echo ($num1/$num2);   
    }

}


?>